                   <div class="recipe-step">
                    <h2>Ingredients</h2>
                    <ul class="ingredients">
                    <li><span>150 gm (3/4th cup) rice flour</span></li>
                    <li><span>60 gm (1/2 cup) sorghum millet (jowar) flour</span></li>
                    <li><span>60 gm (1/2 cup) gram flour (besan)</span></li>
                    <li><span>60 gm (1/2 cup) wheat flour</span></li>
                    <li><span>1 tbsp coriander seed powder</span></li>
                    <li><span>1 tsp cumin seed powder</span></li>
                    <li><span>½ tsp turmeric powder</span></li>
                    <li><span>½ tsp asafetida powder</span></li>
                    <li><span>1 tsp red chili powder</span></li>
                    <li><span>½ cup plain yogurt or dahi or tamarind water</span></li>
                    <li><span>1 ½ tsp salt or to taste</span></li>
                    <li><span>1 cucumber, grated</span></li>
                    <li><span>1 carrot, grated</span></li>
                </ul>
            </div>
            <hr>
            <div class="recipe-step">
                <h2>Instructions</h2>
                <div class="instructionns">
                    <div class="item">
                        <div class="num"></div>
                        <p><span>1 .</span> In a large bowl combine all the dry ingredients, the flour and the spices. Mix with a fork, make a well in the centre and add yogurt and the grated vegetables. If your cucumber is bitter,
                             make sure that you remove the bitterness by rubbing a slice over the cut cucumber until all the bitter juices ooze out.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>2 .</span> Mix well and make a dough. Add a few spoons of water if required to make a soft dough.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>3 .</span> Keep a griddle or a pan on the heat.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>4 .</span>Grease a medium sized (enough to fit over a chakla) baking 
                            parchment or butterpaper with oil. Grease your hands with some oil.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>5 .</span> Take a portion of the dough and pat it over the paper, to get a circular 6” to 8” disc, Make 3 to 4
                             holes with your fore finger near the centre of the disc.
                             This will make sure that the oil seeps into the corners of the dough making a crisp thalipeeth.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>6 .</span> Place the paper over the hot pan and carefully peel off the paper.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>7 .</span>Spread a few drops of oil around the edges of the thalipeeth. After the base is cooked and dark brown spots appear,
                             flip it over and cook on the other side as well.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>8 .</span> Grease the paper again and repeat the above procedure until you finish up the dough.</p>
                    </div>
                </div>
