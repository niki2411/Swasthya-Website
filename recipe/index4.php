                    <h2>Ingredients</h2>
                    <ul class="ingredients">
                    <li><span>1 cup yellow moong dal (split yellow lentils)</span></li>
                    <li><span>4 cups water</span></li>
                    <li><span>1 tablespoon ghee (clarified butter)</span></li>
                    <li><span>1 teaspoon cumin seeds</span></li>
                    <li><span>1 teaspoon grated ginger</span></li>
                    <li><span>1 teaspoon turmeric powder</span></li>
                    <li><span>1/2 teaspoon coriander powder</span></li>
                    <li><span>1/2 teaspoon cumin powder</span></li>
                    <li><span>1/4 teaspoon asafoetida (hing)</span></li>
                    <li><span>1 small tomato, chopped</span></li>
                    <li><span>1 medium carrot, chopped</span></li>
                    <li><span>1 small zucchini, chopped</span></li>
                    <li><span>4 cups vegetable broth</span></li>
                    <li><span>Salt to taste</span></li>
                    <li><span>Freshly chopped cilantro leaves for garnish</span></li>
                </ul>
            </div>
