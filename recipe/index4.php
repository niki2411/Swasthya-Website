            <div class="recipe-step">
                <h2>Instructions</h2>
                <div class="instructionns">
                    <div class="item">
                        <div class="num"></div>
                        <p><span>1 .</span> Rinse the moong dal thoroughly under running water until the water runs clear.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>2  .</span> In a large saucepan, add the rinsed moong dal and water.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>3 .</span>Bring it to a boil over medium heat.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>4 .</span> Reduce the heat to low and let the dal simmer for about 15-20 minutes
                             or until it becomes soft and mushy. Stir occasionally and skim off any foam that forms on the surface.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>5 .</span> While the dal is cooking, heat ghee in a separate pan over medium heat.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>6 .</span> Add cumin seeds and let them splutter.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>7 .</span>Add grated ginger, turmeric powder, 
                            coriander powder, cumin powder, and asafoetida. Sauté for a minute until the spices release their aroma.</p>
                    </div>
                    <br>
                    <div class="item">
                        <div class="num"></div>
                        <p><span>8 .</span> Add chopped tomato, carrot, and zucchini to the pan. Sauté for a few minutes
                             until the vegetables become tender.</p>
                </div>
                <br>
                <div class="item">
                    <div class="num"></div>
                    <p><span>9 .</span>Transfer the cooked dal to the pan with the sautéed vegetables. Mix well.</p>
                </div>
                <br>
                <div class="item">
                    <div class="num"></div>
                    <p><span>10 .</span>Add vegetable broth and bring the mixture to a boil.</p>
                </div>
                <br>
                <div class="item">
                    <div class="num"></div>
                    <p><span>11 .</span>Reduce the heat to low and let the soup simmer for another 10 minutes, allowing the flavors to meld together.</p>
                </div>
                <br>
                <div class="item">
                    <div class="num"></div>
                    <p><span>12 .</span> Season with salt to taste. You can also add Ayur Healthcare’s 
                        special Ayurvedic spice blend for an extra touch of flavor (optional).</p>
                </div>
                <br>
                <div class="item">
                    <div class="num"></div>
                    <p><span>13 .</span>Remove from heat and garnish with freshly chopped cilantro leaves.</p>
                </div>
                <br>
                <div class="item">
                    <div class="num"></div>
                    <p><span>14 .</span>Serve the Ayurvedic Moong Dal Soup hot and enjoy the nourishing goodness provided by Ayur Healthcare.</p>
