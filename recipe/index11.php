                        <div class="num"></div>
                        <p><span>5. For the streusel topping, mix together the sugar and flour. Cut the butter into small pieces and work it into the dry mixture using your fingers or a pastry cutter, until it resembles coarse crumbs. Sprinkle the streusel evenly over the cake batter.</span></p>
