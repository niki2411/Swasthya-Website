                    <h2>Ingredients</h2>
                    <ul class="ingredients">
                    <li><span>1/4 cup of unsalted butter</span></li>
                    <li><span>2 1/2 cups of unbleached white flour</span></li>
                    <li><span>2 teaspoons of baking powder</span></li>
                    <li><span>2/3 cups of buttermilk</span></li>
                    <li><span>1 cups of sugar</span></li>
                    <li><span>2/3 cups of water</span></li>
                    <li><span>125 ml (1/2 cup) water</span></li>
                    <li><span>2/3 cups of water</span></li>
                    <li><span>1 1/2 teaspoons of ground cardamom</span></li>
                    <li><span>1/2 teaspoon of salt </span></li>
                </ul>
            </div>
