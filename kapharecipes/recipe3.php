            <div class="recipe-step">
                <h2>Ingredients</h2>
                <ul class="ingredients">
                    <li><span>1 cup sesame seeds</span></li>
                    <li><span>3/4 cup jaggery (grated)</span></li>
                    <li><span>1 teaspoon fresh ginger (grated)</span></li>
                    <li><span>1 tablespoon ghee</span></li>
                    <li><span>1/4 teaspoon cardamom powder (optional)</span></li>
                </ul>
            </div>
            <hr>
            <div class="recipe-step">
                <h2>Instructions</h2>
                <div class="instructions">
                    <div class="item">
                        <p><span>1. Roast Sesame Seeds:</span> Heat a pan and dry roast sesame seeds over low heat until they turn light golden and aromatic. Set aside to cool.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>2. Prepare Jaggery Syrup:</span> In the same pan, heat ghee and add the grated jaggery. Stir on low heat until the jaggery melts and forms a smooth syrup. Add grated ginger and mix well.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>3. Combine:</span> Add the roasted sesame seeds and cardamom powder (if using) to the jaggery syrup. Mix quickly until all the seeds are well coated with the syrup.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>4. Shape Laddoos:</span> While the mixture is warm, grease your palms with ghee and shape small portions into round laddoos. Allow them to cool completely.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>5. Serve:</span> Store in an airtight container and enjoy as a healthy snack or dessert.</p>
                    </div>
