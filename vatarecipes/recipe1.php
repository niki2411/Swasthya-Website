            <div class="recipe-step">
                <h2>Ingredients for Makki ki Roti</h2>
                <ul class="ingredients">
                    <li><span>2 cups cornflour (makki ka atta)</span></li>
                    <li><span>Water (as needed)</span></li>
                    <li><span>1/4 tsp salt</span></li>
                    <li><span>Ghee or butter (for applying on the roti)</span></li>
                </ul>
            </div>
        
            <hr>
        
            <div class="recipe-step">
                <h2>Instructions for Makki ki Roti</h2>
                <div class="instructions">
                    <div class="item">
                        <p><span>1. Prepare the Dough:</span> In a large bowl, combine the cornflour (makki ka atta) and salt. Gradually add water to knead the dough until it is soft and smooth. Let it rest for 15-20 minutes.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>2. Shape the Roti:</span> Divide the dough into small balls and roll each ball into a flat round shape using parchment paper to prevent sticking.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>3. Cook the Roti:</span> Heat a tava (griddle) over medium heat. Cook the roti on both sides, pressing gently for it to puff up. Apply ghee or butter on top and serve hot.</p>
                    </div>
                </div>
            </div>
        
            <hr>
        
            <div class="recipe-step">
                <h2>Ingredients for Sarson ka Saag</h2>
                <ul class="ingredients">
                    <li><span>4 cups mustard greens (sarson ka saag), chopped</span></li>
                    <li><span>1 cup spinach (palak), chopped</span></li>
                    <li><span>1 onion, finely chopped</span></li>
                    <li><span>1 tomato, pureed</span></li>
                    <li><span>2-3 green chilies, chopped</span></li>
                    <li><span>1 tbsp ginger-garlic paste</span></li>
                    <li><span>1/2 tsp turmeric powder</span></li>
                    <li><span>1 tsp red chili powder</span></li>
                    <li><span>1/2 tsp garam masala powder</span></li>
                    <li><span>1 tsp cumin seeds</span></li>
                    <li><span>1/4 cup cornflour (makki ka atta)</span></li>
                    <li><span>Salt to taste</span></li>
                    <li><span>1 tbsp ghee or oil</span></li>
                    <li><span>1/4 cup water (adjust as needed)</span></li>
                </ul>
            </div>
        
            <hr>
        
            <div class="recipe-step">
                <h2>Instructions for Sarson ka Saag</h2>
                <div class="instructions">
                    <div class="item">
                        <p><span>1. Cook the Greens:</span> In a large pot, add the mustard greens and spinach with a little water. Cook for 15-20 minutes until tender. Then, puree the greens mixture.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>2. Prepare the Masala:</span> In a pan, heat ghee or oil and add cumin seeds. Saut√© chopped onions until golden. Add ginger-garlic paste, green chilies, and cook for a minute.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>3. Add Tomatoes and Spices:</span> Add tomato puree, turmeric powder, red chili powder, and salt. Cook until the oil separates from the masala.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>4. Combine Greens with Masala:</span> Add the pureed greens to the pan and cook on low heat for 15-20 minutes. Stir in cornflour to thicken the saag.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>5. Garnish and Serve:</span> Garnish with ghee and serve the sarson ka saag hot with makki ki roti on the side.</p>
