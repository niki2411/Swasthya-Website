            <div class="recipe-step">
                <h2>Ingredients for Chickpea and Pumpkin Stir-Fry with Brown Rice</h2>
                <ul class="ingredients">
                    <li><span>1 cup brown rice</span></li>
                    <li><span>1 cup cooked chickpeas (or canned)</span></li>
                    <li><span>2 cups pumpkin, peeled and cubed</span></li>
                    <li><span>1 tbsp olive oil</span></li>
                    <li><span>1 onion, chopped</span></li>
                    <li><span>2 garlic cloves, minced</span></li>
                    <li><span>1 tsp ground cumin</span></li>
                    <li><span>1 tsp ground coriander</span></li>
                    <li><span>1/2 tsp chili powder</span></li>
                    <li><span>Salt and pepper, to taste</span></li>
                    <li><span>1 tbsp lemon juice</span></li>
                    <li><span>Fresh cilantro, chopped (for garnish)</span></li>
                </ul>
            </div>
        
            <hr>
        
            <div class="recipe-step">
                <h2>Instructions for Chickpea and Pumpkin Stir-Fry with Brown Rice</h2>
                <div class="instructions">
                    <div class="item">
                        <p><span>1. Cook the Rice:</span> In a pot, cook the brown rice according to package instructions. Set aside once cooked.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>2. Sauté Onions and Garlic:</span> In a large pan, heat the olive oil over medium heat. Add the chopped onion and sauté until translucent, about 5 minutes. Add the minced garlic and sauté for another minute.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>3. Add the Pumpkin:</span> Add the cubed pumpkin to the pan and cook for 10 minutes, stirring occasionally, until the pumpkin softens.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>4. Add the Spices:</span> Stir in the ground cumin, ground coriander, chili powder, salt, and pepper. Cook for another 3-4 minutes, allowing the spices to release their flavors.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>5. Add the Chickpeas:</span> Add the cooked chickpeas to the pan and stir everything together. Let it cook for another 5 minutes to allow the flavors to meld.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>6. Serve with Rice:</span> Serve the chickpea and pumpkin stir-fry over the cooked brown rice. Drizzle with lemon juice and garnish with fresh cilantro.</p>
                    </div>
                </div>
