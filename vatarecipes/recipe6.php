            <div class="recipe-step">
                <h2>Ingredients for Sweet Potato and Spinach Curry with Quinoa</h2>
                <ul class="ingredients">
                    <li><span>1 cup quinoa</span></li>
                    <li><span>2 medium sweet potatoes, peeled and cubed</span></li>
                    <li><span>2 cups fresh spinach</span></li>
                    <li><span>1 onion, chopped</span></li>
                    <li><span>2 garlic cloves, minced</span></li>
                    <li><span>1 can (400g) diced tomatoes</span></li>
                    <li><span>1 can (400g) coconut milk</span></li>
                    <li><span>1 tbsp curry powder</span></li>
                    <li><span>1 tsp ground turmeric</span></li>
                    <li><span>1/2 tsp ground cumin</span></li>
                    <li><span>Salt and pepper, to taste</span></li>
                    <li><span>1 tbsp olive oil</span></li>
                    <li><span>Fresh cilantro, chopped (for garnish)</span></li>
                </ul>
            </div>
        
            <hr>
        
            <div class="recipe-step">
                <h2>Instructions for Sweet Potato and Spinach Curry with Quinoa</h2>
                <div class="instructions">
                    <div class="item">
                        <p><span>1. Cook the Quinoa:</span> Rinse the quinoa under cold water. In a pot, add 2 cups of water and bring it to a boil. Add the quinoa, reduce the heat, cover, and cook for 15 minutes until the water is absorbed. Remove from heat and set aside.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>2. Sauté Onion and Garlic:</span> In a large pot, heat the olive oil over medium heat. Add the chopped onion and sauté until translucent, about 5 minutes. Add the minced garlic and cook for another minute.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>3. Add the Spices:</span> Stir in the curry powder, ground turmeric, ground cumin, salt, and pepper. Cook for 1-2 minutes to release the flavors.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>4. Add Sweet Potatoes and Tomatoes:</span> Add the cubed sweet potatoes and diced tomatoes to the pot. Stir to combine, and cook for 5 minutes.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>5. Add Coconut Milk:</span> Pour in the coconut milk and bring the curry to a simmer. Cook for 15-20 minutes, or until the sweet potatoes are tender.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>6. Add Spinach:</span> Stir in the fresh spinach and cook for another 3-4 minutes until wilted.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>7. Serve:</span> Serve the curry over the cooked quinoa. Garnish with fresh cilantro and enjoy!</p>
                    </div>
