                <h2>Ingredients for Butternut Squash Soup</h2>
                <ul class="ingredients">
                    <li><span>1 medium butternut squash, peeled, seeded, and cubed</span></li>
                    <li><span>1 medium onion, chopped</span></li>
                    <li><span>2 cloves garlic, minced</span></li>
                    <li><span>4 cups vegetable broth</span></li>
                    <li><span>1 tsp ground cumin</span></li>
                    <li><span>1 tsp ground ginger</span></li>
                    <li><span>1/2 tsp ground turmeric (optional)</span></li>
                    <li><span>Salt, to taste</span></li>
                    <li><span>Freshly ground black pepper, to taste</span></li>
                    <li><span>1 tbsp olive oil</span></li>
                    <li><span>1/4 cup coconut milk (optional, for creaminess)</span></li>
                    <li><span>Fresh cilantro, for garnish (optional)</span></li>
                </ul>
            </div>
        
