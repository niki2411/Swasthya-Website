            <div class="recipe-step">
                <h2>Instructions for Butternut Squash Soup</h2>
                <div class="instructions">
                    <div class="item">
                        <p><span>1. Roast the Butternut Squash:</span> Preheat your oven to 400°F (200°C). Toss the cubed butternut squash with olive oil, cumin, ginger, turmeric, salt, and black pepper. Spread the squash on a baking sheet in a single layer and roast for 25-30 minutes, or until tender and lightly browned.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>2. Sauté the Aromatics:</span> While the squash is roasting, heat a large pot over medium heat and add a little olive oil. Sauté the chopped onion and minced garlic until softened, about 5-7 minutes.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>3. Combine the Ingredients:</span> Once the squash is roasted, add it to the pot with the sautéed onion and garlic. Pour in the vegetable broth and bring the mixture to a simmer. Let it cook for 10 minutes, allowing the flavors to meld together.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>4. Blend the Soup:</span> Use an immersion blender to puree the soup until smooth, or transfer the mixture to a blender in batches. Blend until creamy and smooth.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>5. Add Creaminess (optional):</span> If you'd like a creamier soup, stir in coconut milk and heat through.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>6. Season to Taste:</span> Taste the soup and adjust the seasoning with more salt, pepper, or spices if needed.</p>
                    </div>
                    <br>
                    <div class="item">
                        <p><span>7. Serve:</span> Ladle the soup into bowls, garnish with fresh cilantro (if desired), and serve warm with a side of crusty bread.</p>
